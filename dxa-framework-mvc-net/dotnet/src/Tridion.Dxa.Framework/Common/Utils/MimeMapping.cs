        public static string GetMimeMapping(string filename)
        {
            if (filename == null) throw new ArgumentNullException(nameof(filename));
            var ext = Path.GetExtension(filename);
            if (Mappings.ContainsKey(ext)) return Mappings[ext];
            return Mappings[".*"];
        }
